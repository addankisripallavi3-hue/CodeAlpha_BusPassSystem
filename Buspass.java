public class BusPass {
	    String name;
	    int age;
	    String source;
	    String destination;
	    double price;

	    public BusPass(String name, int age, String source,
	                   String destination, double price) {

	        this.name = name;
	        this.age = age;
	        this.source = source;
	        this.destination = destination;
	        this.price = price;
	    }
	}
